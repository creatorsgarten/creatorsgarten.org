    <h2>Who are we?</h2>
    <p>
      <strong>We</strong> are a diverse group of people who are united by our love for creating things.
      We believe that through the lens of creation and technology, we can discover the fun and beauty
      in everything around us.
    </p>
    <p>
      <strong>We</strong> don't believe in disciplinary boundaries. As we think that all knowledge is
      connected, and that by breaking down barriers we can learn more and create more interesting things.
    </p>
    <p>
      <strong>We</strong> connect people through events, meetups, hackathons, and much more. We strive
      to create an environment where creators can come together to collaborate, share ideas, and learn
      from each other.
    </p>
    <p>We stand for:</p>
    <p>
      <strong>Creativity</strong>: We believe that creativity is a fundamental human quality that
      should be nurtured and encouraged. We believe that everyone has the potential to be creative,
      and that the world is a better place when we all express our creativity.
    </p>
    <p>
      <strong>Empathy</strong>: We believe in putting ourselves in others' shoes, and using our
      skills to make a positive impact on the world.
    </p>
    <p>
      <strong>Fun</strong>: We believe that work should be enjoyable, and that every day is an
      opportunity to learn something new and have some fun.
    </p>
    <p>
      The name <strong>Creatorsgarten</strong> comes from the word creators and garten, as in Kindergarten,
      which means garden of children in German. We chose this name because we want to create a garden
      for all creators where they can come to create, learn, and share.
    </p>
